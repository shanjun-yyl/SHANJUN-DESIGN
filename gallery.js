function orderGalleryImages(key, images) {
  if (key !== "brand") {
    return images;
  }

  const featuredImages = [];
  const featuredSet = new Set(brandFeaturedImages);
  const regularImages = [];
  const longImages = [];

  brandFeaturedImages.forEach((src) => {
    if (images.includes(src)) {
      featuredImages.push(src);
    }
  });

  images.forEach((src) => {
    if (featuredSet.has(src)) {
      return;
    }

    if (brandLongImages.has(src)) {
      longImages.push(src);
    } else {
      regularImages.push(src);
    }
  });

  return featuredImages.concat(regularImages, longImages);
}

