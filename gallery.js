function renderGalleryBatch(size = galleryBatchSize) {
  if (renderedImageCount >= galleryImages.length) {
    return [];
  }

  const fragment = document.createDocumentFragment();
  const nextImageCount = Math.min(renderedImageCount + size, galleryImages.length);
  const renderedItems = [];

  for (let index = renderedImageCount; index < nextImageCount; index += 1) {
    const item = createGalleryItem(galleryImages[index], index);
    renderedItems.push(item);
    fragment.appendChild(item);
  }

  renderedImageCount = nextImageCount;
  masonry.appendChild(fragment);

  if (galleryMotion) {
    galleryMotion.observeItems(renderedItems);
  }

  if (renderedImageCount >= galleryImages.length) {
    loadMoreSentinel.remove();
  }

  return renderedItems;
}

function setupGalleryBatchLoader() {
  if (!shouldBatchGallery || renderedImageCount >= galleryImages.length) {
    return;
  }

  if ("IntersectionObserver" in window) {
    const batchObserver = new IntersectionObserver((entries) => {
      const shouldLoadMore = entries.some((entry) => entry.isIntersecting);

      if (!shouldLoadMore) {
        return;
      }

      renderGalleryBatch();

      if (renderedImageCount >= galleryImages.length) {
        batchObserver.disconnect();
      }
    }, {
      rootMargin: "560px 0px",
      threshold: 0
    });

    batchObserver.observe(loadMoreSentinel);
    return;
  }

  function handleScrollLoad() {
    if (window.innerHeight + window.scrollY < document.body.offsetHeight - 900) {
      return;
    }

    renderGalleryBatch();

    if (renderedImageCount >= galleryImages.length) {
      window.removeEventListener("scroll", handleScrollLoad);
    }
  }

  window.addEventListener("scroll", handleScrollLoad, { passive: true });
}


function setupGalleryKineticMotion() {
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)");

  if (reduceMotion.matches || !window.gsap || !window.ScrollTrigger) {
    return {
      observeItems() {}
    };
  }

  const gsap = window.gsap;
  const ScrollTrigger = window.ScrollTrigger;

  gsap.registerPlugin(ScrollTrigger);
  gsap.config({ nullTargetWarn: false });
  document.body.classList.add("motion-enhanced");

  gsap.set([".gallery-heading-copy h1", ".gallery-heading-copy p", ".gallery-wordmark"], {
    autoAlpha: 0,
    yPercent: 115,
    scaleX: 1.18,
    scaleY: 0.72,
    clipPath: "inset(100% 0 0 0)",
    transformOrigin: "left bottom"
  });

  gsap.timeline({ defaults: { ease: "expo.out" } })
    .to(".gallery-heading-copy h1", {
      autoAlpha: 1,
      yPercent: 0,
      scaleX: 1,
      scaleY: 1,
      clipPath: "inset(0% 0 0 0)",
      duration: 1.35
    })
    .to(".gallery-heading-copy p", {
      autoAlpha: 1,
      yPercent: 0,
      scaleX: 1,
      scaleY: 1,
      clipPath: "inset(0% 0 0 0)",
      duration: 1.05
    }, "-=0.72")
    .to(".gallery-wordmark", {
      autoAlpha: 1,
      yPercent: 0,
      scaleX: 1,
      scaleY: 1,
      clipPath: "inset(0% 0 0 0)",
      duration: 1.18,
      onComplete: () => gsap.set([".gallery-heading-copy h1", ".gallery-heading-copy p", ".gallery-wordmark"], {
        clearProps: "transform,opacity,visibility,clipPath"
      })
    }, "-=0.78");

  const prepareGalleryItems = (items) => {
    if (!items.length) {
      return;
    }

    gsap.set(items, {
      autoAlpha: 0,
      y: 120,
      scale: 0.94,
      scaleY: 0.86,
      clipPath: "inset(20% 0 20% 0)",
      transformOrigin: "50% 100%"
    });
  };

  const revealGalleryItems = (items) => {
    if (!items.length) {
      return;
    }

    gsap.to(items, {
      autoAlpha: 1,
      y: 0,
      scale: 1,
      scaleY: 1,
      clipPath: "inset(0% 0 0% 0)",
      duration: 1.12,
      stagger: 0.06,
      ease: "power4.out",
      onComplete: () => gsap.set(items, {
        clearProps: "transform,opacity,visibility,clipPath"
      })
    });
  };

  if ("IntersectionObserver" in window) {
    const pendingItems = new Set();
    const itemObserver = new IntersectionObserver((entries) => {
      const visibleItems = [];

      entries.forEach((entry) => {
        if (!entry.isIntersecting || !pendingItems.has(entry.target)) {
          return;
        }

        pendingItems.delete(entry.target);
        itemObserver.unobserve(entry.target);
        visibleItems.push(entry.target);
      });

      if (visibleItems.length) {
        revealGalleryItems(visibleItems);
      }
    }, {
      rootMargin: "0px 0px -12% 0px",
      threshold: 0.08
    });

    return {
      observeItems(items) {
        prepareGalleryItems(items);
        items.forEach((item) => {
          pendingItems.add(item);
          itemObserver.observe(item);
        });
      }
    };
  }

  return {
    observeItems(items) {
      prepareGalleryItems(items);
      revealGalleryItems(items);
    }
  };
}

